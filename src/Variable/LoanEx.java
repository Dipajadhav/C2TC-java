package Variable;

public class LoanEx {
	
	private int id;
	private String place;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "LoanEx [id=" + id + ", place=" + place + ", name=" + name + "]";
	}
	
	}


